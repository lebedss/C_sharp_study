// Значения b1, k1, b2 и k2 задаются пользователем.
// b1 = 2, k1 = 5, b2 = 4, k2 = 9 -> (-0,5; -0,5)

//  В условии задачи говорится,что пользователь сам каждый раз вводит значения - это намекает о том что точка пересечения не всегда существует.
//  Прямые, заданные уравнениями y = k1 * x + b1 и y = k2 * x +b2 , параллельны(не пересекаются), если их  коэффициенты равны: k1 = k2 .
//  Прямые, заданные уравнениями y = k1 * x + b1 и y = k2 * x +b2 совпадают если коэффициенты b1 и b2 равны (один отрезок)
//  В противном случае если k1 != k2 && b1 != b2  - результатом пресечения прямых заданных уравнениями y = k1 * x + b1 и y = k2 * x +b2  будет точка с координатами x и y
//  где  x = (b2 - b1) / (k1 - k2) , а вторая координата  y = k1 * (b2 - b1) / (k1 - k2) +b1

using static System.Console;

WriteLine("Введите коэффициенты b1, k1, b2 , k2 чтобы найти точку пересечения двух прямых, заданных уравнениями y = k1 * x + b1, y = k2 * x + b2: ");
WriteLine($"Точка пересечения по X: {SegmentsIntersectionPoint(double.Parse(ReadLine()!), double.Parse(ReadLine()!), double.Parse(ReadLine()!), double.Parse(ReadLine()!))}");

double SegmentsIntersectionPoint(double b1, double k1, double b2, double k2)
{
    double x = 0;
    if (b1 == b2)
    {
        WriteLine("Прямые совпадают");
    }
    else if (k1 == k2)
    {
        WriteLine("Прямые параллельны и не пересекаются");
    }
    else
    {
        x = (b2 - b1) / (k1 - k2);
    }
    WriteLine($"Точка пересечения по Y: {(k1 * (b2 - b1)) / (k1 - k2) + b1}");
    return x;
}